# Program of working with int and float variables 

#declaring variables of int type
num1 = 10
num2 = 20

#declaring variables of float type
num3 = 10.5
num4 = 20.5

print("Value: {} Type: {}".format(num1, type(num1)))
print("Value: {} Type: {}".format(num2, type(num2)))
print("Value: {} Type: {}".format(num3, type(num3)))
print("Value: {} Type: {}".format(num4, type(num4)))
print()

#Arithmatic operations on integer variables
print("Operations on integer variables")
print("Addition :{}".format(num1+num2))
print("Subtraction :{}".format(num1-num2))
print("Multiplication :{}".format(num1*num2))
print("Division :{}".format(num1/num2))
print("Modulus :{}\n".format(num1%num2))

#Arithmatic operations on float variables
print("Operations on float variables")
print("Addition :{}".format(num3+num4))
print("Subtraction :{}".format(num3-num4))
print("Multiplication :{}".format(num3*num4))
print("Division :{}".format(num3/num4))
print("Modulus :{}\n".format(num3%num4))

